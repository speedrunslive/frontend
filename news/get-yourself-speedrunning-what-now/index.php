<h1>So you joined a Get Yourself Speedrunning race. What now?</h1>
<p>When we originally thought of the idea for a series of post-AGDQ races, we didn’t expect that it would be nearly as well-received as it was. If some wacky internet prophet had come out of the woodwork and told us we would be fielding a race with 301 entrants, and that RaceBot would be able to support it, we would have told him to get lost.
</p><p>
In the end, all 3 races greatly exceeded our expectations. The final results can be found here:
<br><b>SMB3:</b> <a href="http://www.speedrunslive.com/raceresult/#!/124968"><u>http://www.speedrunslive.com/raceresult/#!/124968</u></a>
<br><b>ALttP:</b> <a href="http://www.speedrunslive.com/raceresult/#!/125224"><u>http://www.speedrunslive.com/raceresult/#!/125224</u></a>
<br><b>Sonic 2:</b> <a href="http://www.speedrunslive.com/raceresult/#!/125899"><u>http://www.speedrunslive.com/raceresult/#!/125899</u></a>
</p><p>
Now, a question remains unanswered from many of these participants:
<br>“I joined your races, but I don’t feel any more of a speedrunner. What do I do now?”
</p><p> 
If you joined one of our races, and tried to complete the game quickly, then you’ve already taken a big step towards getting into speedrunning. Maybe you didn’t get a top time. Maybe you placed dead last. But if you tried to beat it quickly, that’s speedrunning in our book. So before anyone can tell you what to do now, there’s a question you need to answer for yourself.
</p>
<h2>What do YOU want to do now?</h2>
<p>
Approaching speedrunning is a lot simpler than most newcomers assume it is. There’s a reason that when a prospective speedrunner asks “what game should I pick up?” the answer they’ll get 99% of the time is “a game you like.” That’s it. You find a game that you enjoy so much, the prospect of beating it tens, hundreds, thousands of times or more is not such a daunting concept. So let’s help you find what YOU want to do now.
</p><p>
Did you join one of the Get Yourself Speedrunning races, and enjoyed the game so much that you could see yourself playing it over and over again?
<br>Did you not like the game you played, but you could see enjoying speedrunning if you found the right one?
<br>Did you like joining the race and competing against other players, but can’t see yourself running it outside of a race?
<br>Did you have no fun whatsoever, and could not possibly see enjoying speedrunning anything?
</p><p>
Only one of these 4 questions results in you discovering that speedrunning is probably not for you. If you answered “yes” to any of the first 3 questions, then you should put in the effort to get more involved in something you enjoy!
</p><p>
If you answered “yes” to the first question, the answer is already known. Start with the game you already know you enjoyed racing, and keep playing it!
</p><p>
If you answered “yes” to the second question, then you know the answer is out there somewhere. Think about games you’ve played in your youth, or played recently, or watched others play and thought looked fun. This is where every single speedrunner you’ve ever known started. We either remembered or encountered a game that looked fun, started playing it, and then beat that game over and over.
</p><p>
If you answered “yes” to the third question, you don’t need to reset like crazy to enjoy speedrunning! You can simply get your fill by joining races as they come up. Often there are weekly races scheduled for games: SMB3’s are on Friday @ 10pm EST, and Zelda: ALttP’s are on Sundays @ 3pm EST. You may even find that by racing the game, you wind up wanting to run it on your own after all.
</p><p>
So think about it. Think about a game you like so much, you would legitimately enjoy playing it over and over.
</p>
<h2>Then play it over and over!</h2>
<p>
Once you know what game you want to run, there are multiple resources at your disposal:
	<ul>
		<li>Look to see if a game you’re interested in has a channel on our IRC by typing <code>/list</code> in the client at any time. Get to know the community! Ask if there’s an existing guide or tutorial to getting into the game.</li>
		<li>Browse the forums at <a href="http://speeddemosarchive.com"><u>speeddemosarchive.com</u></a> and <a href="http://speedrun.com"><u>speedrun.com</u>.</a></li>
		<li>Review the existing record (if available), and analyze how the runner progresses through the game piece by piece, room by room. Scrutinize every decision they make.</li>
		<li>Review the existing tool-assisted run at tasvideos.org (if available). If you’d like to get more in-depth, watch the run in an emulator, and view the controller inputs frame-by-frame to get to know how the run does what it does.</li>
		<li>You may also want to review kakka_rot’s intro guide <a href="https://forum.speeddemosarchive.com/post/the_basic_protocol_of_routing_a_speedrun_speedrunning_essay.html"><u>here</u></a>, which goes into much greater detail than this news post.</li>
	</ul>
</p><p> 
But above all else, just play the game! Even if you only play through the game once a night for a week, you should see your time steadily improve, simply from becoming increasingly familiar with the game. You don’t need to do a lick of research to start speedrunning.
</p>
<h2>Join upcoming events!</h2>
<p>
In addition to all this, after the success of Get Yourself Speedrunning, we plan on holding many more racing events in the future, so look out for those! Two events coming up to know about include:
<ul>
	<li>The SRL <a href="http://mystery.beatthega.me/index.htm"><u>Mystery Tournament #6</u></a>, starting February 27th. The idea of this event is that every participant submits 3-4 games into a pool when they enter. Each round of the tournament is a series of 1v1 matches where the competitors draw a randomly selected game from the pool, and face off to see who can beat the suggested goal first. Goals are intended to be no longer than 45 minutes, which makes this a particularly good entry point for a new speedrunner.</li>
	<li>Badbrakes' <a href="https://docs.google.com/document/d/130dkwwf4kxEyD6LbfWoE5imPZmcDxV4RoD00otpeyU8"><u>NES marathon race</u></a> on February 28th. While not directly hosted by SRL, since Badbrakes announced this idea it has really taken off, and many speedrunners have begun practicing the games that the tournament includes in anticipation.</li>
</ul>
</p><p>
So if you are wondering what the next step is for you, we hope this guide has been helpful. Find a game you like, and just keep playing it. Get more involved with the community. Watch as your times improve just from beating it regularly. If you’re able to get that far, believe it or not, you are speedrunning!
</p>


