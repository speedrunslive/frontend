<h1>Get Yourself Speedrunning: A Series of 3 Weekly Post-AGDQ Races</h1>
<p>
At this point, everyone is familiar with Awesome Games Done Quick. 
Every successive year, the marathon brings in not only more money for charity, 
but also more viewers who have never seen speedrunning before. 

</p><p>
It's almost tradition that following the event, speedrunning forums, chat servers, and twitch channels are swarmed 
by users all asking the same question: "Hey this speedrunning thing looks fun, how can I get involved too?"
</p><p>
We want to provide this opportunity. Every Saturday from Jan. 17th - Jan. 31st, we are going to host a race of a competitive yet newbie-friendly game. Each race is open to anyone who'd like to enter, whether they are the record holder or have never touched a controller before.
<p>
For those that can't join, we are going to be re-streaming some of the top runners of the game on our <u><a href="http://twitch.tv/speedrunslive">twitch channel</a></u>, featuring commentary by some of our favorite hosts in the community.

Details on each race are as follows. Featured racers may change pending any last minute surprises.
</p>
<br>
