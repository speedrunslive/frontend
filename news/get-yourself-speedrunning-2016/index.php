<h1>Get Yourself Speedrunning: A Series of 3 Weekly Post-AGDQ Races</h1>
<p>
We're excited to bring back our Get Yourself Speedrunning races a third time, following AGDQ 2016! These races are designed to answer the popular question "Hey this speedrunning thing looks fun, how can I get involved too?" This time around, we chose games that are easy to get into, but have high skill ceilings. We feel we found the perfect selection, with a set that is sure to trigger the nostalgia in all of us.
 </p><p>
For those that can't join, we are going to be re-streaming some of the top runners of the game on our <a href="http://twitch.tv/speedrunslive"><u>Twitch channel</u></a>, featuring commentary by some of our favorite hosts in the community. Details on each race are as follows. Featured racers may change pending any last minute surprises. 
</p>
<br>
