<h2>Tournaments</h2>
<p>


The most exciting change from our previous GYS events is that after the races, we will be holding an open tournament for each game. You may be familiar with previous tournaments in the speedrunning community such as our <a href="http://speedrunslive.challonge.com/srlmystery7"><u>Mystery Tournament series</u></a>, or the outrageously successful <a href="http://challonge.com/zeldaone"><u>The Legend of Zelda</u></a> tournament organized by the game's community.
 </p><p>
