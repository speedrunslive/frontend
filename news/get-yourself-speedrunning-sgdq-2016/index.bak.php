<h2>Tournaments</h2>
<p>
Like last time, we will be featuring a double elimination tournament for each game. These tournaments are open entry, meaning that people of all skill levels are free to join. The tournaments from last GYS were a huge success, capping out at 63 players! If you're thinking about it, give it a try! Registration and start dates will be announced shortly before SGDQ.
</p>
<p>
	And of course, keep your eyes peeled for Mystery Tournament 9 coming up soon! The idea of the event is that when a player signs up, he or she submits a small set of games into a large game pool. As entrants compete in 1v1 races, they pull a game from this pool. You never know what you might get, and that's what makes it perfect for veteran and novice speedrunners alike! Information on registering for MT9 will come shortly after SGDQ 2016. </p>
