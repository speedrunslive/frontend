<h1>Get Yourself Speedrunning: A Series of 3 Weekly Post-SGDQ Races</h1>
<p>
You wanted it, you've got it! Get Yourself Speedrunning returns immediately after SGDQ 2016, bringing you 3 HUGE races with live commentary coverage, as well as comprehensive tournaments to keep the action going throughout this summer and fall. As always we've made sure to choose 3 games which we feel make excellent speedruns, and which we think will appeal to both existing speedrunners who want to branch out, and potential runners looking for a solid game idea to start out with. </p><p>
