<h1>Hitbox.tv Support</h1> 
<p>During the past few months we've looked into supporting Hitbox.tv on the site, 
but development was held up due to how their VOD system works. In the current Hitbox functionality, users are required to manually save their VODs in order to view them at a later time. 
Additionally, VODs frequently do not save properly, through no fault of the user.<br> </p>
<p>Because this makes it significantly harder to provide proof upon request, and could potentially cause significant confusion and problems when racing, <br>
we feel it's best to wait until Hitbox improves their VOD functionality before fully supporting streaming via Hitbox for races.</p>

<h2> What this means is the following: </h2>
<ul style="margin-left:25px">
	<li>You are able to .setstream hitbox/channel in the main channel or any race channel</li>
	<li>You will be able to stream to the frontpage of SpeedRunsLive using a Hitbox.tv stream</li>
	<li>You will <b>NOT</b> be featured on the races page using a Hitbox.tv stream</li>
	<li>When racing a game that requires streaming, you must set your stream to a Twitch.tv stream with archiving enabled as per <u><a href="http://www.speedrunslive.com/faq/#proofcall">the rules</a></li></u>
</ul>

We will continue to look into fully integrating Hitbox.tv for the site.</p>
