<h1 id="seasons">Seasons</h1>
<p>Since season 1 ended last August, the answer to whether or not it was ultimately successful was never truly clear. However from looking at the site's <a href="../../races/#!/racestats">race stats</a>, the 2 months in SRL’s history that saw the highest race counts were April and August of last year, the months when season 1 began and ended. So from a quantitative standpoint, season 1 was hugely successful.
</p><p>
However, it had three major flaws:
</p>
<h2>Duration</h2>
<p>Season 1 ran way too long. It was scheduled to run from April - July, and then ran an additional month on top of that. 3 months would have been too long as it is, but 4 months really pushed it. If we bring back seasons, we would like to adjust the format to be:
</p><p>
1. Announcement and game selection
<br>2. 1 month of practice
<br>3. 1-2 month season
<br>4. 1 month of downtime before next announcement
</p><p>
We feel that this is the appropriate amount of time to allow a given season, so that it is kept exciting for its full duration without overstaying its welcome.
</p>
<h2>Presentation on site</h2>
<p>Currently the 4-month period where season 1 took place is its own little void on the leaderboards, which fell between the pre-season and post-season time period that’s being called Legacy. To resolve this, we'd like to have all races, regardless of whether or not they took place during a season, count towards Legacy. Season races would count not only towards legacy, but also towards their respective season.
</p>
<h2>Lack of awards</h2>
<p>The season 1 rewards were never implemented. To put it simply, when season 2 resumes this is something we will definitely revisit, as we feel it adds a fun incentive to participating in the event.
</p>
<br>
<p>Once we resolve these 3 issues, we believe that the adapted format from season 1 will be tremendously successful. We have not yet investigated the technical implications of our proposed solutions; before we proceed with this, we wanted to reach out to the community for your ideas. We’d like to hear what criticisms you have regarding season 1, as well as your thoughts on the format we proposed. If you believe you have a better working format, please feel free to let us know.
</p>

