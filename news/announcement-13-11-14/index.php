<h1 id="frontpage">Front page</h1>
<p>We feel that racing will always be the primary feature of SRL. However, the front page has evolved into a significant part of the community; it has quickly become a popular resource for watching an extensive collection of speedrunning streams, and we want to accommodate that.
</p><p>
We’ve been listening to your feedback over the past year, and feel we have resolved the most common criticisms regarding the front page and its functionality:
</p>
<h2>Lack of transparency in front page whitelisting</h2>
<p>The front page was originally intended to cater to speedrunners who made regular use of SRL. As such, becoming featured required either fulfilling hidden race requirements, or being a member of the Twitch team. As speedrunning took off, there were more and more streamers that our userbase wanted to see on the front page, but who did not regularly race. We began to manually whitelist many of these individuals to accommodate the requests, but it only resulted in confusion over what was required to appear on the front page.
</p><p>
We have decided that we would like to shift the focus of the SRL front page to make it a place where all speedrunning streams can be listed, not just those who race.
</p><p>
The requirements for getting on the front page will be kept public, and they are as follows:
</p>
<ul>
<li>Having your stream set via .setstream in #speedrunslive on IRC,</li>
<li>One hour of total race time (per account lifetime), AND</li>
<li>Visiting the site while logged in OR joining the #speedrunslive channel while being identified on IRC (each month).</li>
</ul>
<p>
With this we hope to list as many speedrunning streams as possible, and to get rid of the ambiguous whitelisting concept that has caused much confusion and frustration for the community in the past. 
</p><p>
We will continue to whitelist individual marathons, assuming they meet the requirements listed in the <a href="../../faq/#charitymarathons">FAQ</a>.
</p>
<h2>Varying opinions on what should be featured on the front page</h2>
<p>Not everyone will always agree on what should be featured on the front page. In order to address this level of subjectivity we have added a customizable interface, allowing you to design your own front page as you please. You will be able to log in using the same account you use on IRC. From there you may hide games or players to put them at the bottom of the list, or pin them, which will give them a special section at the top of the front page. All of this will be saved in your profile for each time you visit.
</p>
<h2>Non-speedrunning content frequently appears on the front page</h2>
<p>Content that is <a href="../../faq/#whatcanistream">not related to speedrunning</a> should not be featured on SRL. To make it easier for the user, we've added several options in your profile that will allow you to choose whether or not you want to be featured. One of the options that we have included is called auto-detect, an idea originally conceived by pksage. Using this option, you can include particular keywords in your Twitch title to be featured, as well as certain keywords that will make you not show up. These keywords can be found on the Edit Profile page once you log in. You will also be able to set your stream options through RaceBot.
</p><p>
In addition to this, we have added a tool for SRL staff members which allows them to remove a stream using the following <a href="../update-13-11-14">warning system</a>. If you are removed, you will receive an e-mail reminding you to not stream casual games or channel design, and your display option will temporarily be toggled so as to not display you on SRL.
</p><p>
We are hoping that after a while we won't need to use the warning system very often, as people will either take advantage of the auto-detect option or set their front page preference to "Do not display me" when appropriate.
</p>
<h2>Inability to sort by game</h2>
<p>Several sorting options have been added to the front page.
</p><p>
This includes a list view of games currently being streamed, and a search filter which takes Twitch title, streamer name, and game name into account. You may pin or hide streams via the game sort view as needed.
</p>
<br>
<p>We've made a more technical article, which outlines the above features and several others we’ve added as well. You can find that article <a href="../update-13-11-14"> here</a>.
</p>

