<h1 id="leaderboards">Leaderboards</h1>
<p>When the SRL leaderboards project was first conceptualized and brought to the public, the team looked to build a system to address varying community needs based on the ideas they had collected. As the project evolved, development slowed, and the project did not progress at the rate the development team or the general community expected. This was largely due to both its sheer complexity and the personal lives of the developers who volunteered to help.
</p><p>
As it stands, the leaderboards Pac has been working on at <a href="http://www.speedrun.com">speedrun.com</a> currently offer much of the functionality desired by the SRL leaderboards team. Through discussion with Pac, those involved with SRL leaderboards development have decided to offer their support to Pac in developing a centralized leaderboards resource at speedrun.com.
</p><p>
The SRL moderation team fully supports this decision, and is not interested in continuing development on a competing leaderboards site. Thus, as it stands, development on leaderboards at SRL has ceased for the foreseeable future. We're optimistic about where this will take the project, and look forward to seeing the leaderboards grow successfully so that the speedrunning community can have the valuable resource it has desired for so long.
</p>

