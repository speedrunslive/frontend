<h1 id="whatsnext">What’s next for SRL</h1>
<h2>Front-end rewrite</h2>
<p>Our next big focus will be on the front-end of the website. We have already started work on this, and the results we’ve seen so far tell us that overall navigation will run much more smoothly. The noticeable things will all be minor, but when combined it gives the website an overall better finish.
</p>
<h2>Season 2</h2>
<p>We want to make races as exciting as possible, and we think bringing back seasons is the way to go about it. We previously described our ideas on how to make season 2 more compelling, and we'd love to hear your feedback to ensure its success.
</p>
<h2>Event calendar</h2>
<p>Events and marathons are starting to occur so frequently that they often conflict with each other, simply because the organizers have no easy way to know what events are already being planned. Mike89 recently took the initiative to build and maintain an existing <a href="https://www.google.com/calendar/embed?src=uv6fsckqs38ai69iate0ed8los%40group.calendar.google.com">event calendar</a>.
</p><p>
We would like to expand upon this idea by including not only marathons and events, but also scheduled races, weekly races, and whatever else might fit. Each individual type of event would be toggleable on/off.
</p>
<h2>Additional front page features</h2>
<p>We're very excited about the new front page changes, and have a lot of ideas to improve upon it even further. Initial ideas include the option to import your Twitch follows to your SRL front page, and allowing the ability to display multiple streams at a time.
</p>
<h2>Getting involved with the indie gaming scene</h2>
<p>In the past we have reached out to 3 separate indie game developers to hold scheduled races of their respective games. These include <a href="http://noumenongames.com/" target="_blank">Noumenon Games</a> (<a href="http://www.speedrunslive.com/raceresult/#!/41702">Nimbus</a>), <a href="http://www.magicaltimebean.com/" target="_blank">MagicalTimeBean</a> (<a href="http://www.speedrunslive.com/races/result/#!/95424">Escape Goat 2</a>), and <a href="http://www.futurlab.com/" target="_blank">FuturLab</a> (<a href="http://www.speedrunslive.com/races/result/#!/110968">Velocity Ultra</a>).
</p><p>
These events were all tremendously successful, fielding over 30 entrants each, and it was very exciting to work with the developers to help promote their games. We plan on keeping an eye out for promising upcoming indie titles in the future, and will be in contact with the developers in case they’d like to collaborate on more events like these.
</p>

