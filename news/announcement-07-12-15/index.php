<h2>Mystery Tournament 7</h2>
<p>
<i>
Registration starting August 7th<br>
Tournament begins September 5th
</i>
</p>
 <p>
The Mystery Tournament is one of our favorite events to host, and is always a popular one. The idea is that when you join the tournament, you submit a small set of games into a game pool. As people compete in 1v1 races, they pull a game from this pool. You never know what you might get, and that's what makes it perfect for veteran and novice speedrunners alike!
 </p><p>
More info and previous brackets available at the <a href="http://mystery.beatthega.me/index.htm"><u>Mystery Tournament website</u></a>.  Be sure to follow the <a href="http://twitter.com/SRLmystery"><u>SRL Mystery twitter</u></a> for announcements as registration approaches!
</p>
