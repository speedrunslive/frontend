<h2>Speed Gaming League Finals</h2>
<p>
<i>
Throughout October - November<br>
</i>
<u><a href="http://twitch.tv/speedgaming">http://twitch.tv/speedgaming</a></u>
</p>
 <p>
The Speed Gaming League is a competition organized by feasel and mithical9, designed to test a player's ability to learn a game quickly under a strict time constraint. Four participants each get a week to practice a specific goal of a given game. After that week of preparation, the players race to see who can get the fastest completion time of the goal within 30 minutes. Also featured each week is a mystery game, where the players get a mere 1 hour to learn and compete in a specific goal.
 </p><p>
SGL has held over 50 matches since it began in 2013, and during that time has pinpointed some of the best players in the speedrunning community. This fall, these players as well as some new faces will be going head to head to determine a champion of the Speed Gaming League.
 <p>
You can find full details about SGL at <a href="http://speedgaming.tv"><u>http://speedgaming.tv</u></a> or follow SGL on twitter at <a href="http://twitter.com/SpeedGamingShow"><u>http://twitter.com/SpeedGamingShow</u></a>. 
More information on the tournament will be revealed there as it becomes available.
 </p>
