Véase más abajo para más normas relativas a los emuladores.</p>

<h1 id="whatseasons">¿Qué son exactamente las "seasons" (sesiones)?</h1>

<p>A season is a 3 month, ranked competition of races. The first season 
Una sesion tiene una duración de 3 meses y es básicamente un ranking de competición de carreras.  La primera sesion empezó el 12 de Abril del 2013 y las próximas sesiones empezarán pronto o en cuanto la primera termine. Es obligatorio transmitir (stream) durante las metas de la sesión correspondiente,independientemente de si estás jugando en la consola o emulador. Para obtener más información sobre las sesiones, haga clic <a href="/races/#%21/seasons/1"> aquí</a>.</p>

<h1 id="setstream">¿Cómo hago para que mi transmisión aparesca en la página de carreras de SpeedRunsLive?</h1>

<p>Primero, cuando te unas a tu primera carrera debes asegurarte de que tu stream esté vinculado a RaceBot, para hacer eso simplemente escribe ".setstream twitch.tv/tunombredeusuario" (sin comillas).Después de terminar la primera carrera, ésta será grabada y automáticamente tu perfil de usuario será creado. Después de eso cada vez que participes en una carrera tu transmisión aparecerá en la página de carreras de SRL.</p>

<h1 id="nostream">¿Qué sucede si no puedo transmitir pero aún quiero participar de las carreras?</h1>

<p>Es completamente preferible que SI hagas transmisión al momento de competir en alguna carrera, puesto que de lo contrario se te pedirá mostrar pruebas de que realmente competiste en ella para hacer legal todo. Al contrario de ésto si es que no presentaras pruebas podrías ser descalificado (y posiblemente baneado). Si estás jugando en un emulador o si estás en una <a href="/races/#!/seasons/1">meta de sesion</a> (season goal) el requisito de transmitir es obligatorio (SIN excepción). Si estás jugando en una consola no es requisito transmitir, para más detalles sobre las transmisiones y comprobar pruebas lea más abajo.</p>

<h1 id="proofcall">Reglamento sobre transmisiones y pruebas</h1>

<h2>General</h2>
<p>La siguiente explicación podría estar sujeta a cambios y/o podrían crearse instancias en que los operadores de SpeedRunsLive deberán tomar distintas decisiones que podrían no estar explicadas en éste texto dependiendo de la situación.</p>

<p>Transmitir no es requerimiento para todas las carreras en SpeedRunsLive, pero es recomendable que si lo hagasm no sólo por nuestro bien sino que por el tuyo propiamentetal. Leer especificiaciones de transmisión más abajo.</p>

<p>Ser descalificado de una carrera puede no ser necesariamente por el motivo de que seas tramposo (cheater), sino que también puedes ser descalificado por fallar en la muestra de algún comprobante o prueba de que si corriste la carrera.Leer especificiaciones de pruebas más abajo.</p>

<h2>Transmisión (streaming)</h2>

<p><b>Consolas:</b> Las consolas pueden ser transmitidas mediante una capturadora de video conectada a tu computador o usando una camara web apuntando hacia tu televisor.No estás obligado a transmitir si es que estás jugando en una consola, pero si estás obligado a mostrar pruebas de que realmente competiste en la carrera (si es que éste fuera el caso) para comprobar tu tiempo en ella.</p>

<p><b>Emuladores:</b> Todos los emuladores deben ser transmitidos durante las carreras, es un requisito obligatorio para todos los emuladores y no existe excepción (algunos emuladores podrían estar baneados).Si estás pensando competir en un emulador de un telefono Android u otro dispositivo puede que no tengas permiso de participar en la carrera HASTA QUE un operador en #speedrunslive lo permita.Si se pusiera en duda de si tu emulador afecta en la carrera o no y se asume que si entonces no debes competir en la carrera sin permiso para no afectar a los demás participantes.En el caso de que no transmitas tu carreras, ésto podría conllevar a que muestres pruebas verídicas de que si participaste en ella, mientras alguien dude de tu tiempo.</p>

<p>Cómo referencia <b>los siguientes emuladores están globalmente baneados en su uso durante las carreras:</b></p>
<ul>
	<li><b>SNES</b> - ZSNES, snes9x 1.4x</li>
	<li><b>N64</b> - Project64 2.x</li>
</ul>

<p><b>Juegos de PC:</b> Todos los juegos de computador deben ser transmitidos durante las carreras,es un requisito obligatorio para todos los juegos de computador y no existe excepción.Si crees que no puedes transmitir cierto juego puesto que al hacerlo el juego produce demaciado lag o no tienes suficiente ancho de banda para la transmisión entonces no deberías de competir en la carrera a menos que tengas permiso de un operador de #speedrunslive. Fuera de eso no se otorgará para juegos como Minecraft,Minesweeper,Windows Desktop Games u otros con bajos requisitos.Los juegos de pc podrán ser transmitidos en cualquier computador comprado en los últimos cuatro años. Aún así podrías se llamado a mostrar pruebas en caso de que tengas permiso para participar en la carrera y no transmitas,si es que alguien duda de tu tiempo.</p>

<p><b>Otros:</b> Cuando tengas alguna duda pregunta a un operador o a algún usuario experimentado para evitar problemas.</p>

<h2>Pruebas</h2>
<p>La petición de pruebas es la forma en que tratamos a los tramposos (cheaters) y dudosos de sus tiempos en SpeedRunsLive. Si un corredor acaba con el tiempo que parece estar por encima de su nivel de habilidad o si un nuevo corredor de repente obtiene un tiempo bueno en la tabla de posiciones, es común por iniciativa pedir que muestre pruebas de la carrera que hizo y no transmitió. Las peticiónes de pruebas pueden ser solicitadas por cualquiera, sea o no en la carrera misma y cualquier persona puede solicitar una muestra de pruebas sin importar la posición o cargo.Por lo general se solicita asistencia de un moderador (voice) u operador de #speedrunslive para manejar éste tipo de situaciones.</p>

<p>Si es que llegase a ocurrir la meta (goal) será establecida como "don't record" (no grabar) o "proofcall in progress" (muestra de pruebas en progreso) y la carrera en cuestión deberá certificar pruebas suficientes para ser grabada, así también los otros participantes de la carrera deberán quedar conformes con la muestra de pruebas para que sea archivada.</p>

<p>Ejemplo de pruebas suficientes:</p>

<ul>
	<li>Transmitir en buena calidad con sonido siempre será prueba suficiente.</li>
	<li>Grabaciones de video en buena calidad.</li>
	<li>En algunos juegos una captura de pantalla del nombre del archivo y progreso de éste es suficiente.</li>
	<li>En algunos juegos una captura de pantalla del juego (incluyendo timer o nivel) será prueba suficiente.</li>
	<li>En algunos juegos una explicación de la ruta utilizada y llaves eventualmente tomadas serán prueba suficiente.</li>
</ul>

<p>Éstos ejemplos son generals y son involuntariamente imposibles de tomarlos como "pruebas suficientes",por lo cual recomendamos SIEMPRE transmitir las carreras para evitar problemas.</p>

<p>Si sucediera de que te pidan pruebas y estás imposibilitado de probar tu tiempo, se resolverá como se hizo anteriormente y no necesariamente serás considerado como tramposo (cheater), aunque todo dependerá de la gravedad del tema, podrías ser descalificado de la carrera y en el peor de los casos podríamos creer que si eres un tramposo por lo cual serás baneado de SpeedRunsLive.</p>

<p>Si piensas marcharte mientras se te solicitan las pruebas o produces un período de inactividad muy largo, serás descalificado de la carrera y si fuese necesario serás baneado de SpeedRunsLive.</p>

