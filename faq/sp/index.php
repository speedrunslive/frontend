<h1 id="howgoals">¿Podrían explicarme como funcionan las metas (goals)? ¿Qué significa any%?</h1>

<p>Básicamente para competir en una carrera se necesita de un punto de partida y un punto de meta, el únto de meta es considerado como "goal" el más simple de todos es "any%" lo cual significa que debes terminar el juego. El término proviene de la comunidad de Metroid, donde la gente podría hacer carreras de 100%, low% (terminar el juego con la menor cantidad de items) y any% (cualquier método mientras que se termine el juego).</p>

<p>Algunos juegos no necesariamente funcionan con porcentajes, como Super Mario 64, el cual por lo general se cataloga a nivel de estrellas. Una carrera de "120 estrellas" en SM64 puede ser considerado como 100% y es básicamente empezar un nuevo juego, adquirir las 120 estrellas y terminar el juego (no crees metas si es que no tienes conocimiento previo del juego, asegurate de preguntar a un voice o operador para evitar problemas).</p>

<p>Algunas metas son un poco más complicadas como por ejemplo: en King's Bounty una de las metas más populares es "continentia bounties; no island leaving no week ending (normal)", en lo anterior se detalla el fin de la carrera y se establecen ciertas reglas como "normal" (lo cual apunta a la dificultad del juego) y a veces se especifica los pasos que se deben realizar para ser considerada una carrera finalizada.</p>

<p>Para más términos, revisa nuestro <a href="/faq/glossary/">Glosario</a>.</p>

<h1 id="rankingsystem">¿Cómo funciona el sistema del ranking?</h1>

<p>El ranking para cada juego es calculado usando una version modificada de <a href="http://research.microsoft.com/en-us/projects/trueskill/">Microsoft TrueSkill</a>.</p>

<h1 id="gametime">Algunos juegos ya traen timer.¿Cómo afecta ésto en la carrera?</h1>

<p>No afecta en absoluto. Usamos tiempo real siempre, por ende el ganador se determina al finalizar siempre en tiempo real. Los timers que están dentro de los juegos tienden a ser alterados y la mayoría de las veces no cuentan el tiempo muerto tal como el hecho de presionar pausa, transiciones o lag.</p>

<h1 id="turboetc">¿Cómo es el tema de las funciones turbo, abuso de glitchs, codigos cheat, savestates, save-warping, scripts y newgame+?</h1>
<p>Éstas son las especificaciones:</p>

<h2>Funciones Turbo</h2>

<p>No está permitido mientras que el control original si tenga ésta función. La función turbo es injusta para aquellos que no tienen un "third party controller" con función turbo.</p>

<h2>Abuso de glitch</h2>
<p>Permitido pero a veces limitado, una forma de limitar el abuso de glitchs es tener ciertas reglas previamente creadas o categorías en las cuales se estipula la especificación de la(s) restricción(es) o cualquier cosa que no puedas hacer.Generalmente los glitches si son permitidos, en cualquier caso si realmente no entiendes porqué entonces considera lo siguiente: El juego en sí ejecuta un código programado para que responda a lo que tiene que hacer, el juego es y será ley siempre por ende si intentas adquirir "intenciones de desarrollador" de conjeturas tratando de averiguar qué es exactamente lo que hace el juego referente a su código lo más probable es que el juego termine bloqueandose o similar lo cúal recaería en que pierdas tiempo en la carrera y hasta un reinicio del juego completo (haciendote perder tiempo valioso). Como consejo revisa las respectivas wikis de cada juego para obtener mayor información de los posibles realizables glitches y así también contacte con usuarios con experiencia en el juego para aprender más rápido.</p>

<h2>Cheat-codes</h2>
<p>Cualquier dispositivo externo o interno como gameshark o game genie no está permitido. Éstos generalmente rebajan el juego y remueven elementos de habilidades los cuales están en contra de cualquier punto de competición. Si está permitido usar cheats durante una carrera siempre y cuándo esté especificado en las reglas de la meta (goal).</p>

<h2>Save-states</h2>
<p>Los Savestates y cualquier función similar a ésta independiente del emulador/consola/etc están completamente baneados. Sólo son permitidos al momento de no estar en una carrera y netamente por un tema de práctica de algún juego.</p>

<h2>Save-warping</h2>
<p>En algunos juegos si guardas y vuelves a cargar podría llevarte a un warp el cúal podría ser en cierto punto más rápido de lo normal. Por lo general ésto si está permitido pero ésto dependerá de las reglas internas de cada comunidad. Si la comunidad de algún juego en un futuro empezara a hacer rechazo de éste tipo de doctrinas entonces se respetará la opinión de la comunidad y será eliminado.</p>

<h2>Scripts</h2>
<p>Ésto es totalmente decidido por la comunidad del juego en carrera.</p>

<h2>Newgame+</h2>
<p>Permitido siempre y cuando fuese establecido en la meta de la carrera, en caso contrario no se permite.</p>

