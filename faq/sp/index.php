<h1 id="what">¿Qué es Speedrunning?</h1>
<p>Speedrunning no es más que jugar un juego con la intención de terminarlo lo más rápido posible.</p>
<p.Los Speedrunners se desafían mutuamente para llevar el juego hasta el límite y obtener mayor conocimiento del mismo.</p>

<h1 id="whatsrl">¿Qué es SpeedRunsLive?</h1>
<p>SpeedRunsLive es una comunidad basada en speedrunning después de que la popularidad del livestreaming se diera a conocer.</p>
<p>Algunas cosas principales de SRL:</p>
<ul>
	<li>Un <a href="/">directorio de streams</a>, para encontrar transmiciones en vivo de speedruns.</li>
	<li>Una <a href="/races/">plataforma de carreras speedrun</a> ricamente desarrollado.</li>
	<li>Una <a href="/channel/">comunidad popular</a> de IRC</li>
</ul>

<p>Estamos constantemente en desarrollo. La próxima mejora en la que estamos trabajando es en create un sistema de cuentas (para loguearse y editar tu perfil detalladamente), más formas de estar en la página de streams y una tabla de posiciones de SRL (un lugar para publicar carreras(runs) y estar en un ranking contra otros speedrunners).</p>

<h1 id="frontpage">¿Cómo logro aparecer en la página principal de SpeedRunsLive?</h1>

<p>Recientemente la mejor forma de aparecer en la página principal es simplemente haciendo carreras. Los streams que aparecen en la página principal son calculados haciendo uso de un algoritmo que toma en cuenta el total de tiempo de tus carreras, carreras en las que haz participado, el número de carreras que tienes y otros factores. Sólo continua haciendo carreras y aportaras a la comunidad y en la eventualidad deberías de aparecer en el sitio principal. Después de aparecer en la página principal ... continua compitiendo en carreras!</p>

<p>Estamos trabajando en más formas de aparecer en la página principal.</p>

<h1 id="whatcanistream">¿Qué me está permitido transmitir en la página principal de SRL?</h1>

<p>Algunos juegos que mayormente no son catalogados "amistoso" con el speedrunning (como LoL o Starcraft 2) están en nuestra lista negra para que no sean mostrados en la página principal. Si estás concentrado en transmitir ese tipo de juegos escribe <b>[nosrl]</b> en el título de tu transmisión.</p>

<p>Los siguientes son catalogados como "cosas afines con el speedrunning":</p>
<ul>
	<li>speedruns</li>
	<li>practica</li>
	<li>creando una ruta</li>
	<li>carreras</li>
	<li>trabajo TAS</li>
	<li>discusión sobre speedrunning</li>
	<li>casual/desafios o playthroughs de juego que hagas speedruns o intentes hacerlo.</li>
</ul>

<p>Si deseas transmitir otro tipo de cosas escribe <b>[nosrl]</b> en el titulo de tu stream. Algunos ejemplos:</p>
<ul>
	<li>juegos que no tengas intención de hacer speedruns</li>
	<li>trabajo sin catalogar (rediseñar el canal, etc.)</li>
</ul>

<h1 id="whatsrlraces">¿Qué son las carreras?</h1>
<p>Las carreras son la principal faceta de SpeedRunsLive. Las carreras permiten competir directamente con otros jugadores. Los jugadores crean una carrera y RaceBot es que inicia la cuenta regresiva.Al momento de dar la partida (GO!) todos comienzan al mismo tiempo a jugar el juego en cuestión con la intención de terminar en primer lugar.Las carreras son transmitidas en vivo para que otras personas puedan ver toda la acción.</p>
<p>Todos los juegos en SRL tienen una tabla de posiciones. Los jugadores pueden escalar en ella para obtener una mejor pocisión cuando están compitiendo.Cuanto más fuerte sea el oponente, más obtendrás al ganar. Cuanto más debil sea más tendrás que perder.Por encima de todo, todos estamos en esto para pasar un buen rato.Las carreras tienen un exelente potencial de competición pero al final de todo simplemente se trata de diversión sana.</p>

<h1 id="join">¿Cómo participo en las carreras de SRL?</h1>
<p>Primero debes registrar tu nickname en el IRC para competir en las carreras. Más detalles <a href="/faq/registration">aquí</a>. Después de eso, simplemente <a href="/channel/">entra en #speedrunslive</a> y entra en una carrera.</p>
<p>Debes utilizar el mismo nombre de usuario para todas las carreras en las que quieras participar así el sitio podrá almacenar tus datos perfectamente.</p>
<p>No utilices dos nombres de usuario diferentes!</p>

<h1 id="howchannel">¿Cómo funciona el canal?</h1>
<p>#speedrunslive es un canal de IRC. Si no estás familiarizado con IRC no te preocupes. Puedes conectarte y participar de las carreras facilmente <a href="/channel/">a través del sitio</a>. Una vez estés dentro puedes interactar con RaceBot, él controla las carreras.</p>
<p>Servidor: irc.speedrunslive.com<br>
Canal: #speedrunslive</p>
<p>Recomendamos usar un cliente IRC, así también existe un widget en nuestro sitio para poder participar (si es que no sabes utilizar IRC). Si deseas utilizar un cliente IRC, pero aun no tienes uno, recomendamos que utilices mIRC. Para ver un video tutorial de como conectarse a SpeedRunsLive a través de mIRC, mira <a href="https://www.youtube.com/watch?v=hxTBHia1wak">éste tutorial</a>.</p>
<p>Puedes entrar en #speedrunslive-s para tener una mejor ayuda en español.</p>

<h1 id="racebot">¿Cómo utilizo a RaceBot?</h1>

<p>Para aquellos que les gustan los video tutoriales, Twig trabajó en <a href="http://www.youtube.com/watch?v=6Spocso3nNs&amp;list=PLPi2qr4PQ7f51bbIW_h0VtKy7e01rc3Or&amp;noredirect=1">éste video</a> donde muestra ¿cómo funciona RaceBot?</p>

<p>RaceBot es un bot que organiza todas las carreras en SRL. Aceptará los comandos que escribas dentro del canal. Revisa la <a href="/faq/commandlist/">lista de comandos</a> para detalles más específicos.
Si miras la lista de comandos notarás que existen "Voice Commands" (comandos de usuario Voice), éstos son comandos que sólo esos usuarios o los de mayor rango pueden utilizar.</p>

<p>Si eres nuevo y no estás seguro de cómo empezar una carrera no tengas miedo de preguntar, estamos aquí para ayudarte.</p>

<p>Ten en cuenta que debes empezar carreras sólo utilizando las abreviaciones de los juegos, NO EL NOMBRE DEL JUEGO COMPLETO! Revisa la <a href="/faq/commandlist/">lista de comandos de RaceBot</a> para más detalles.</p>

<h1 id="whatrace">¿Qué carreras puedo jugar en SpeedRunsLive?</h1>

<h2>Creando una carrera</h2>

<p><b>Si tienes un oponente</b>, y ambos tienen el juego que les interesa competir y ambos concuerdan en la meta de la carrera, entonces tu puedes crear el canal de la carrera en el chat utilizando el comando .startrace. Si el juego ya existe en la base de datos (revisa <a href="/races/#%21/gamelist/alphabetical">aquí</a>) puedes usar ".startrace abreviación", sin comillas y reemplazando "abreviación" por la abreviación del juego en cuestión (la abreviacion se encuentra en la columna derecha del juego en cuestión) Por ejemplo: si quisieras crear una carrera de Super Mario 64 y tu tubieras un oponente, sólo debes escribir ".startrace sm64" (sin comillas), puesto que la abreviación de ese juego es esa.</p>
<p>Si el juego no existe en la base de datos puedes usar ".startrace newgame" (sin comillas) y un usuario Voice de #speedrunslive creará el juego en la base de datos cuando terminen la carrera.</p>

<h2>Metas</h2>

<p>Animamos a los usuarios a competir en metas (goals) que sean significativas y otorguen tiempo puedan demostrar sus habilidades.Generalmente cualquier meta para terminar el juego completo bajo cualquier restricción será aceptable, mientas que exeda aproximadamente 3 minutos o más. Si deseas competir en carreras extremadamente cortas como Tower of Heaven, una sugerencia sería establecer una meta (goal) para terminar el juego una cierta cantidad de veces en una sola linea.</p>

<p>Generalmente cualquier meta que permita terminar una parte del juego como "beat world 1" (terminar el primer mundo), debería de ser concluida en al menos 10 minutos aproximadamente, como sea, algunas metas en algunos juegos como por ejemplo: "Beat Brock in Pokemon R/B/Y" (vencer a brook en pokemon rojo/azul/amarillo) puede que exeda los 10 minutos, pero sigue siendo demaciado corto puesto terminar  ese juego en una carrera toma mucho más tiempo del que se presenta.Lo mismo se aplica a metas como "Get 1000 points" o "collect 100 such and suches". Todo ésto es porque en SRL queremos que se represente la habilidad de terminar el juego completo, por ende hacer carreras demaciado cortas pueden establecer que en la tabla de posiciónes se refleje de mala forma.</p>
<p>Si aun tienes dudas respecto de éste tema es conveniente que preguntes a un mod y el te dirá que hacer.</p>

