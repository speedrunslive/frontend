<p> <br>A continuacón se especifica el reglamento para carreras en la comunidad IRC de #speedrunslive.
Recomendamos leer el <a href="/faq/">FAQ</a> completamente, debes familiarizarte con las siguientes reglas o podrías ponerte en riesgo de ser descalificado de una carrera o ser baneado del canal de IRC.</p>

<h1 id="before">Reglas antes de empezar una carrera</h1>
<ul>
<li>Las carreras online sólo pueden ser terminadas por el staff de SRL (op/half-op/mod). Ésto es especificamente asignado en random enfocado a carreras 1vs1 tales como: poker, ajedréz o Starcraft.
	<br><b>Resultado:</b> Carrera terminada.</li><br>
 
<li>No está permitido realizar carreras con 2 o más metas (goals), ejemplo : SM64 70 estrellas vs 120 estrellas, o SM64 70 estrellas vs SMS any%.
	<br><b>Resultado:</b> Carrera terminada.</li><br>
 
<li>No se permite establecer metas demaciado cortas o sin sentido. Revise el <a href="/faq/sp/#whatrace">FAQ</a> para más detalles.
	<br><b>Resultado:</b> Carrera terminada.</li><br>
 
<li>Las carreras en Equipos/coop si están permitidas, pero no serán grabadas.
	<br><b>Resultado:</b> Carrera terminada al finalizar.</li><br>
 
<li>No empezar una carrera a menos de que ya tengas un oponente que se una a ella.
	<br><b>Resultado:</b> Carrera terminada.</li>
</ul>

<h1 id="while">Reglas durante la carrera</h1>
<ul>
<li>Absolutamente prohibidas ciertas funciones de los emuladores tales como savestates o aceleradores. Así mismo no está permitido utilizar funciones turbo-fire a menos si el control original lo permite. No se aceptan dispositivos externos como game ginie o gameshark. No está el uso de scripts a menos que sean especificamente permitidos. Revise el <a href="/faq/sp/#turboetc">FAQ</a> para más detalles.
	<br><b>Resultado:</b> descalificación de la carrera y ban permanente de #speedrunslive. Nos tomamos éste tema MUY seriamente y mantenemos cero tolerancia frente a las trampas.</li><br>
 
<li>Siempre debes transmitir (stream) ya sea si es que estás jugando en emulador o si es que estás jugando un juego de computador. Revise el <a href="/faq/sp/#proofcall">FAQ</a> para más detalles.
	<br><b>Resultado:</b> si estás jugando en un emulador o algún juego de computador sin hacer transmisión (stream), descalificación de la carrera.En el caso de estar jugando en una consola y no tienes ninguna prueba que pueda ser certificada como real serás descalificado de la carrera. (se recomienda hacer uso de una capturadora de video para evitar ésto). Si fueses un posible sospechoso de hacer uso de trampas(cheats) podrías ser movido a una lista especial en la cual se te exigirá siempre hacer transmisión(stream) de las carreras en las que quieras participar.
 
<br><br><b>Nota:</b> en la sección "Videos" de tu configuración de twitch es recomendable que actives la opción "Automatically archive my broadcasts" (archivar automáticamente mis transmiciones). Ésto permitirá que cualquiera pueda ver tus videos después de que detengas tu transmisión. <em>Si transmites en vivo tu carrera y no puedes otorgar un video como pueda veridica de tu carrera, puede que seas descalificado</em>. Ésta opción está por defecto desactivada.</li><br>
 
<li>No puedes hacer uso de los siguientes emuladores de SNES : ZSNES o SNES9x 1.4x. No puedes hacer uso del siguiente emulador de N64 : Project64 2.x.
	<br><b>Resultado:</b> descalificación con advertencia, podrías terminar siendo baneado si se repite.</li><br>
 
<li>Si deseas cambiar la meta (goal) en medio de la carrera, debes estar seguro que todos los participantes que aún estén en desarrollo acepten el cambio.
	<br><b>Resultado:</b> Si al menos una persona no accede al cambio o no responde, la meta se mantendrá.</li><br>
 
<li>No están permitidos breaks demaciado largos, ejemplo: dormir durante una carrera.
	<br><b>Resultado:</b> Descalificación de la carrera.</li>
</ul>

<h1 id="after">Reglas fuera de la carrera</h1>
<ul>
<li>No te conectes a través del servicio tor.
<br><b>Resultado:</b> Ban hasta que no utilices ese servicio.</li><br>
 
<li>No competir en carreras con más de 1 nick.
<br><b>Resultado:</b> Podría terminar en un ban si se repite.</li><br>
 
<li>Si haces transmisión de material inaceptable, podrías ser advertido.
<br><b>Resultado:</b> Si es continuo (o es lo suficientemente grave como para no merecer una advertencia), podrías formar parte de la blacklist de la página principal.</li><br>
 
<li>No está permitido evadir el ban.
</ul>


